<h1>A ideia e motivação do AGIC</h1>
<div class="well">
<p>Com  a ideia de transformar  dados em informação, conscientizando o eleitor da importância de seu voto, foi criado o AGIC.
&nbsp;Um ambiente web com a missão de ajudar o eleitor a votar da melhor maneira possível, trazendo as informações sobre seus candidatos de uma forma interativa, visual e dinâmica.
</p>
<p>
Ao final do projeto temos como objetivo que cada cidadão repense de como seu voto é importante e que sua escolha pode influenciar no destino de sua cidade, seu estado e seu país.<br />
</p>
<p>
<strong>Se um eleitor acertar na escolha teremos a sensação de dever cumprido como cidadão e como desenvolvedores.</strong>
</p>
</div>
